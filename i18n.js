let currentLang = "sk";

async function initI18n() {
  const { language } = await chrome.storage.local.get("language");
  currentLang = language === "en" ? "en" : "sk";
  document.documentElement.lang = currentLang;
  return currentLang;
}

function getLang() {
  return currentLang;
}

async function setLanguage(lang) {
  currentLang = lang === "en" ? "en" : "sk";
  await chrome.storage.local.set({ language: currentLang });
  document.documentElement.lang = currentLang;
}

function t(key, vars = {}) {
  let str = I18N[currentLang]?.[key] ?? I18N.sk[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(`{${k}}`, String(v));
  }
  return str;
}

function formatMinutes(m) {
  if (m >= 60 && m % 60 === 0) return t("hourShort", { n: m / 60 });
  return t("minShort", { n: m });
}

function applyI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
}

function applyIntervalOptions(selectEl) {
  const map = {
    "1": "min1", "2": "min2", "3": "min3", "5": "min5",
    "10": "min10", "15": "min15", "30": "min30", "60": "min60"
  };
  selectEl.querySelectorAll("option[value]").forEach((opt) => {
    if (map[opt.value]) opt.textContent = t(map[opt.value]);
    if (opt.value === "custom") opt.textContent = t("customOption");
  });
}

function updateLangButtons(activeLang) {
  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === activeLang);
  });
}
